# First Principles Business

## Les 5 lois Musk

### Loi 1 — Premiers principes
Décomposer chaque problème jusqu'aux vérités fondamentales. Refuser les analogies et l'héritage cognitif. Question clé : « Qu'est-ce qui est physiquement, mathématiquement, économiquement vrai ici — indépendamment de ce qui se fait habituellement ? »

### Loi 2 — Vision gigantesque
La taille de la vision détermine la qualité des solutions. Un problème 10x plus grand attire des talents 10x meilleurs et force des approches radicalement différentes. Question clé : « Quelle est la version de ce projet qui change vraiment quelque chose à grande échelle ? »

### Loi 3 — Échec comme donnée
L'échec n'est pas un résultat négatif — c'est un point de données. Échouer vite, cheapement, avec apprentissage explicite. Question clé : « Qu'est-ce que cet échec m'a appris que le succès n'aurait pas révélé ? »

### Loi 4 — Compression du temps
Les délais conventionnels sont des conventions sociales, pas des contraintes physiques. Demander : « Si je devais faire ça en 10% du temps prévu, comment ? » Cela force la suppression des étapes non-essentielles.

### Loi 5 — Recruter des cerveaux
Une seule personne exceptionnelle remplace 10 personnes correctes. Le recrutement est une décision stratégique, pas RH. Question clé : « Est-ce que cette personne ferait avancer le projet si elle était seule dessus ? »

## Modes d'usage

| Mode | Déclencheur | Output |
|------|-------------|--------|
| Diagnostic | « Quelque chose ne va pas dans mon approche » | Identification de la loi violée |
| Design | « Je construis quelque chose de nouveau » | Application des 5 lois au projet |
| Décision | « J'hésite entre deux options » | Analyse premiers principes des deux |
| Rétrospective | « Ça a échoué / réussi » | Extraction des apprentissages par loi |

